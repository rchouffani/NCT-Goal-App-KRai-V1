/**
 *  @swagger
 *  /api/goal_type:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Goal_type]
 *      summary: Get all goal_type
 *      description: Get all goal_type
 *      responses:
 *        200:
 *          description: Goal_type list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Goal_type"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await Goal_typeDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'Goal_Type', 'Goal_Type_Details'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

